def decode_hex_flags(hex_input):
    """Decodifica flags en diferentes formatos hexadecimales"""
    
    # Si es una cadena con "0x" separados por espacios
    if isinstance(hex_input, str):
        # Limpiar y convertir a lista de enteros
        hex_values = [int(x, 16) for x in hex_input.split() if x.startswith('0x')]
    else:
        # Si ya es una lista de enteros
        hex_values = hex_input
    
    # Convertir a ASCII
    flag = ''.join(chr(h) for h in hex_values)
    return flag

