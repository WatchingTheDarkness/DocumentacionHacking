    """Convierte valores hexadecimales a texto ASCII"""
    result = ""
    print("ConversiÃ³n hexadecimal a ASCII:")
    print("-" * 50)
    
    for i, hex_val in enumerate(hex_values):
        char = chr(hex_val)
        result += char
        print(f"0x{hex_val:02x} -> '{char}'")
    
    print("-" * 50)
    return result

